package assignments;

public class Assignments61_whileLoop {
    public static void main(String[] args) {
        int count = 1; 

        System.out.println("Counting from 1 to 10:");

        
        while (count <= 10) {
            System.out.println(count); 
            count++; 
        }

        System.out.println("Finished counting.");
    }
}
