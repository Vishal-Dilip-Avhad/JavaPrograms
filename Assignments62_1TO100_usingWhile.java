package assignments;

public class Assignments62_1TO100_usingWhile {
	public static void main(String[] args) {
        int count = 1; 

        System.out.println("Counting from 1 to 100:");

        
        while (count <= 100) {
            System.out.println(count); 
            count++; 
        }

        System.out.println("Finished counting.");
    }
}
